#include "Pair.hpp"

/*********************************************************************************************************
** Function name:           Pair
** Descriptions:            Pair Default Constructor
*********************************************************************************************************/
Pair::Pair() {
	this->name = "";
	this->pid = 0;
}

/*********************************************************************************************************
** Function name:           ~Pair
** Descriptions:            Pair Destructo
*********************************************************************************************************/
Pair::~Pair() {
}

/*********************************************************************************************************
** Function name:           Pair
** Descriptions:            Pair Constructor
*********************************************************************************************************/
Pair::Pair(std::string _name, unsigned int _pid, bool *_func(SoftwareSerial &_HC06)) : name(_name + "\r"), pid(_pid) {
	this->func = _func;
}

/*********************************************************************************************************
** Function name:           getName
** Descriptions:            return name in Pair
*********************************************************************************************************/
std::string Pair::getName() {
	return this->name;
}

/*********************************************************************************************************
** Function name:           getPid
** Descriptions:            return pid in Pair
*********************************************************************************************************/
unsigned int Pair::getPid() {
	return this->pid;
}

/*********************************************************************************************************
** Function name:           startFunc
** Descriptions:            start function
*********************************************************************************************************/
void Pair::startFunc(SoftwareSerial &_HC06) {
	func(_HC06);
}
